        Console.WriteLine("\t\t" + copyright);
        Console.WriteLine("\t" + copyright);
        Console.WriteLine("\t\t" + copyright);
        Console.WriteLine("\t\t\t" + copyright);
        Console.WriteLine("\t\t\t\t" + copyright);
        Console.WriteLine("\t\t\t\t\t" + copyright);
    }
}

